const SIDEBAR_COOKIE_NAME = "sidebar_state"
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_WIDTH = "15rem"
const SIDEBAR_WIDTH_MOBILE = "18rem"
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

type SidebarContextProps = {
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
  hovered: boolean
  setHovered: (hovered: boolean) => void
}

const SidebarContext = React.createContext<SidebarContextProps | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }
  return context
}

function SidebarProvider({
  defaultOpen = true,
  open: openProp,
  onOpenChange: setOpenProp,
  className,
  style,
  children,
  ...props
}: React.ComponentProps<"div"> & {
  defaultOpen?: boolean
  open?: boolean
  onOpenChange?: (open: boolean) => void
}) {
  const isMobile = useIsMobile()
  const [openMobile, setOpenMobile] = React.useState(false)
  const [hovered, setHovered] = React.useState(false)

  const [_open, _setOpen] = React.useState(defaultOpen)
  const open = openProp ?? _open
  const setOpen = React.useCallback(
    (value: boolean | ((value: boolean) => boolean)) => {
      const openState = typeof value === "function" ? value(open) : value
      if (setOpenProp) {
        setOpenProp(openState)
      } else {
        _setOpen(openState)
      }
      document.cookie = `${SIDEBAR_COOKIE_NAME}=${openState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
    },
    [setOpenProp, open]
  )

  const toggleSidebar = React.useCallback(() => {
    return isMobile ? setOpenMobile((open) => !open) : setOpen((open) => !open)
  }, [isMobile, setOpen, setOpenMobile])

  React.useEffect(() => {
    const handleKeyDown = (event: KeyboardEvent) => {
      if (
        event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
        (event.metaKey || event.ctrlKey)
      ) {
        event.preventDefault()
        toggleSidebar()
      }
    }
    window.addEventListener("keydown", handleKeyDown)
    return () => window.removeEventListener("keydown", handleKeyDown)
  }, [toggleSidebar])

  const state = open ? "expanded" : "collapsed"

  React.useEffect(() => {
    if (open) {
      setHovered(false)
    }
  }, [open])

  const contextValue = React.useMemo<SidebarContextProps>(
    () => ({
      state,
      open,
      setOpen,
      isMobile,
      openMobile,
      setOpenMobile,
      toggleSidebar,
      hovered,
      setHovered,
    }),
    [
      state,
      open,
      setOpen,
      isMobile,
      openMobile,
      setOpenMobile,
      toggleSidebar,
      hovered,
      setHovered,
    ]
  )

  return (
    <SidebarContext.Provider value={contextValue}>
      <TooltipProvider>
        <div
          data-slot="sidebar-wrapper"
          style={
            {
              "--sidebar-width": SIDEBAR_WIDTH,
              ...style,
            } as React.CSSProperties
          }
          className={cn(
            "group/sidebar-wrapper flex min-h-svh w-full",
            className
          )}
          {...props}
        >
          {children}
        </div>
      </TooltipProvider>
    </SidebarContext.Provider>
  )
}

function Sidebar({
  side = "left",
  collapsible = "offcanvas",
  className,
  children,
  ...props
}: React.ComponentProps<"div"> & {
  side?: "left" | "right"
  collapsible?: "offcanvas" | "none"
}) {
  const {
    isMobile,
    state,
    openMobile,
    setOpenMobile,
    hovered,
    setHovered,
    open,
  } = useSidebar()

  // Track collapsing transition: keeps expanded styling while animating out
  // eslint-disable-next-line @typescript-eslint/no-unused-vars
  const [_collapsing, setCollapsing] = React.useState(false)
  const prevOpenRef = React.useRef(open)

  React.useEffect(() => {
    // Detect open -> closed transition
    if (prevOpenRef.current && !open) {
      setCollapsing(true)
    }
    prevOpenRef.current = open
  }, [open])

  const handleMouseLeave = React.useCallback(() => {
    setHovered(false)
  }, [setHovered])

  if (collapsible === "none") {
    return (
      <div
        data-slot="sidebar"
        className={cn(
          "flex h-full w-(--sidebar-width) flex-col bg-sidebar text-sidebar-foreground",

          className
        )}
        {...props}
      >
        {children}
      </div>
    )
  }

  if (isMobile) {
    return (
      <Sheet open={openMobile} onOpenChange={setOpenMobile} {...props}>
        <SheetContent
          data-sidebar="sidebar"
          data-slot="sidebar"
          data-mobile="true"
          className="w-(--sidebar-width) bg-sidebar p-0 text-sidebar-foreground [&>button]:hidden"
          style={
            {
              "--sidebar-width": SIDEBAR_WIDTH_MOBILE,
            } as React.CSSProperties
          }
          side={side}
        >
          <SheetHeader className="sr-only">
            <SheetTitle>Sidebar</SheetTitle>
            <SheetDescription>Displays the mobile sidebar.</SheetDescription>
          </SheetHeader>
          <div className="flex h-full w-full flex-col">{children}</div>
        </SheetContent>
      </Sheet>
    )
  }

  return (
    <div
      className="group peer hidden text-sidebar-foreground md:block"
      data-state={state}
      data-hovered={hovered ? "true" : undefined}
      data-collapsible={state === "collapsed" ? "offcanvas" : ""}
      data-side={side}
      data-slot="sidebar"
    >
      {/* Sidebar gap - only takes up space when expanded (not when hover-peeking) */}
      <div
        data-slot="sidebar-gap"
        className={cn(
          "relative bg-transparent transition-[width] duration-300 ease-linear",
          "group-data-[side=right]:rotate-180",
          open ? "w-(--sidebar-width)" : "w-0"
        )}
      />

      {/* Single sidebar container that transitions between expanded / hover-peek / collapsed */}
      <div
        data-slot="sidebar-container"
        onMouseEnter={() => {
          if (!open && !hovered) {
            setHovered(true)
          }
        }}
        onMouseLeave={() => {
          if (!open && hovered) {
            handleMouseLeave()
          }
        }}
        // onTransitionEnd={handleTransitionEnd}
        className={cn(
          "fixed inset-y-0 z-10 hidden md:flex",
          side === "left" ? "left-0" : "right-0",
          open
            ? // Expanded: full height, border, width transition
              "h-svh w-(--sidebar-width) transition-[left,right,width,margin,border-radius,box-shadow] duration-300 ease-linear group-data-[side=left]:border-r group-data-[side=right]:border-l"
            : hovered
              ? // ::before creates an invisible hover safe zone: extends left to cover the margin gap and right for a generous buffer
                cn(
                  "shadow-3xl z-50 m-2 h-[calc(100svh-1rem)] w-(--sidebar-width) rounded-lg border border-sidebar-border opacity-100 transition-[opacity,width] duration-300 ease-in-out",
                  "before:absolute before:-inset-2 before:inset-y-0 before:-right-8 before:content-['']"
                )
              : // Collapsed: zero width, ::after hover zone extends out
                cn(
                  "h-svh w-0 opacity-0 transition-[opacity,width] duration-300 ease-in-out",
                  "after:absolute after:top-0 after:bottom-0 after:w-4 after:opacity-100 after:content-['']",
                  side === "left" ? "after:left-0" : "after:right-0"
                ),
          className
        )}
        {...props}
      >
        <div
          data-sidebar="sidebar"
          data-slot="sidebar-inner"
          className="relative flex h-full w-full flex-col overflow-hidden rounded-[inherit] bg-sidebar"
        >
          {/* Expand trigger - only shown during hover-peek */}

          {children}
        </div>
      </div>
    </div>
  )
}

function SidebarTrigger({
  className,
  onClick,
  ...props
}: React.ComponentProps<typeof Button>) {
  const { toggleSidebar } = useSidebar()

  return (
    <Button
      data-sidebar="trigger"
      data-slot="sidebar-trigger"
      variant="ghost"
      size="icon"
      className={cn("size-8", className)}
      onClick={(event) => {
        onClick?.(event)
        toggleSidebar()
      }}
      {...props}
    >
      <PanelLeftIcon className="size-5.5" />
      <span className="sr-only">Toggle Sidebar</span>
    </Button>
  )
}

